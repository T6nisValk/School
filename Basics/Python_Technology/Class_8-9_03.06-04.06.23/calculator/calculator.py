"""This is a calculator"""


class Cal:
    """Calculator"""

    @staticmethod
    def mul(a, b):
        """Calulates the multiplication"""
        return a * b

    @staticmethod
    def div(a, b):
        """Caluclates the division"""
        return a / b

    @staticmethod
    def sub(a, b):
        """Calculates the subtraction"""
        return a - b

    @staticmethod
    def add(a, b):
        """Calculates the addition"""
        return a + b
